            <h3>Initiatives
                <hr>
            </h3>
            <p>
            The assessment of the exposure and socio-economic effects of women agricultural entrepreneurs highlights significant challenges they face, including limited access to resources, training, and financial support, which restrict their ability to thrive in the sector. Gender disparities are especially evident in disaster management, where women often bear a disproportionate burden due to societal roles and lack of inclusion in decision-making processes. These disparities further exacerbate the vulnerability of women in agriculture during climate-related disasters. In addition, farmers have generally perceived little impact of the National Greening Program (NGP) on their overall well-being, with many reporting that it has not led to noticeable improvements in their livelihoods, food security, or environmental stability. This suggests a need for more targeted interventions and a deeper understanding of the specific challenges faced by these communities to ensure the effectiveness of such programs.            </p>

