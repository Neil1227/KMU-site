<section class="content-section mb-3" id="sdg2">
    <div class="text-column">
            <p>
                Pampanga State Agricultural University (PSAU) actively contributes to Sustainable Development Goal 2 (SDG 2): Zero Hunger, particularly by working toward the target of doubling agricultural productivity and increasing the incomes of small-scale food producers—including women, indigenous peoples, and family farmers.</p>
            <p>
                Utilizing the rich agricultural resources of the region, PSAU transforms various local commodities into value-added products that create sustainable livelihood opportunities for growers. This initiative empowers farmers to achieve financial independence by equipping them with innovative technologies that enhance farm productivity and profitability.</p>
            <p>
                In addition to livelihood support, PSAU conducts cutting-edge research to address pressing agricultural and aquaculture challenges. One such study explores the use of Balakat (Ziziphus talanai), an indigenous plant, as a natural molluscicide, providing farmers with a locally available, eco-friendly solution to pest infestations.</p>
            <p>
                To further its commitment to food security and nutrition, PSAU has also developed innovative food technologies using tamarind—a fruit abundant on campus. These include tamarind-based vinegar, wine, and even ice cream, showcasing the university’s dedication to sustainable agricultural transformation and entrepreneurship.</p>
